package com.epam.training.lordoftherings.domain;

public class Ring {
	private int power;
	private String name;
	
	
	
	public Ring() {

	}

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
